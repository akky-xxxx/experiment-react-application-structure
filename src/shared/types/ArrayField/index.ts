/**
 * `useFieldArray` の `fields` が返すレコード構造に対応する
 * 配列フィールド用の汎用ラッパー型です。
 *
 * この型は、配列要素の値を `value` プロパティとして保持する
 * オブジェクト構造を表します。React Hook Form の `useFieldArray`
 * において、各フィールドレコードをアプリケーション側で
 * 型安全に扱うための用途を想定しています。
 *
 * フォームデータやアプリケーションのデータモデルにおいて、
 * 配列要素をオブジェクトとしてラップすることで、
 * 一貫した構造で処理できるようにします。
 *
 * @template T 配列要素として保持される値の型。
 *
 * @property {T} value
 * 配列要素の実際の値を保持するプロパティ。
 */
export type ArrayField<T> = { value: T }
